package com.rtw181204.ex88firebasechatting;

public class User {


    public String id;
    public String name;
    public String nick;
    public String profile;
    public String uid;


    public User(String id, String name, String nick, String profile, String uid) {
        this.id = id;
        this.name = name;
        this.nick = nick;
        this.profile = profile;
        this.uid = uid;
    }

    public User() {
    }
}
